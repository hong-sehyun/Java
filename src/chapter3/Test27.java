package chapter3;

public class Test27 {

	public static void main(String[] args) {

		// for문
		for(int i = 11; i < 11; i++) {
			System.out.println("for OK");
		}
		
		//while 문
		int j = 11;
		while(j < 11) {
			System.out.println("while OK");
			j++;
		}
		
		//do while 문
		int k = 11;
		do {
			System.out.println("do-while OK");
			k++;
		} while(k < 11);
				
		
	}

}
