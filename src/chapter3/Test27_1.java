package chapter3;

public class Test27_1 {

	public static void main(String[] args) {

		// for문
		for(int i = 1; i < 11; i++) {
			System.out.println(i);
		}
		
		//while 문
		int j = 1;
		while(j < 11) {
			System.out.println(j);
			j++;
		}
		
		//do while 문
		int k = 1;
		do {
			System.out.println(k);
			k++;
		} while(k < 11);
				
		
	}

}
